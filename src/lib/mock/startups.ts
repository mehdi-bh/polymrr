// ---------------------------------------------------------------------------
// MRR history — 6 months of mock snapshots per startup.
// In production this comes from daily cron snapshots.
// ---------------------------------------------------------------------------

function generateMrrHistory(currentMrr: number, months: number): MrrSnapshot[] {
  const snapshots: MrrSnapshot[] = [];
  const now = new Date();
  for (let i = months; i >= 0; i--) {
    const date = new Date(now);
    date.setMonth(date.getMonth() - i);
    const variance = 0.7 + (i === 0 ? 0.3 : (months - i) / months) * 0.6;
    const mrr = Math.round(currentMrr * variance);
    snapshots.push({
      date: date.toISOString().split("T")[0],
      mrr,
    });
  }
  return snapshots;
}

